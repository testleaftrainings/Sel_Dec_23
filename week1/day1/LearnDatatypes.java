package week1.day1;

public class LearnDatatypes {
	//short cut to run -> ctrl+f11

	public static void main(String[] args) {
		//  datatype variable = value	
		int	age = 31;
		long phoneNumber = 9876543210l;
		float mileage = 35.4f;
		double bankBalance = 7000.809;
		char logo='H';
		String brandName = "Honda";
		boolean isPresent=true;

		System.out.println("Age is : "+age);
		System.out.println("The PhoneNumber is :"+phoneNumber+ " ,  Mileage is :"+mileage);
		System.out.println(bankBalance);
		System.out.println(logo);
		System.out.println("The brandName is : "+brandName+ ", Boolean value : "+isPresent);
		
	}

}
